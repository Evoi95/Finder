package entity;


public class Gioco {

    private int idGioco;
    private String name;
    private String type;

    public int getIdGioco() {
        return idGioco;
    }

    public void setIdGioco(int idGioco) {
        this.idGioco = idGioco;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTect() {
        return tect;
    }

    public void setTect(String tect) {
        this.tect = tect;
    }

    private String tect;




}
